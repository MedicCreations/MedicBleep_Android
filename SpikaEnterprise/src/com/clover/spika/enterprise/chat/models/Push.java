package com.clover.spika.enterprise.chat.models;

public class Push {

    private String id;
    private String message;

    public Push() {
    }

    public String getId() {
	return id;
    }

    public void setId(String id) {
	this.id = id;
    }

    public String getMessage() {
	return message;
    }

    public void setMessage(String message) {
	this.message = message;
    }

}
